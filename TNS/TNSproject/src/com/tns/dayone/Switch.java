package com.tns.dayone;

public class Switch {
	public static void main(String [] args)
	{
		char x='c';
		switch (x)
		{
		case't':
		case'b':
			System.out.println(x+"for panda");
			break;
			
		case'd':
		case'm':
			System.out.println(x+"for loin");
			break;
			
		case'c':
		case'x':
			System.out.println(x+"for ship");
			break;
		}
		
		
		}

}
