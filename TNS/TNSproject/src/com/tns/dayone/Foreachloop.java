package com.tns.dayone;

public class Foreachloop {
	public static void main(String [] args)
	{
		int a[]= {10,20,30,40,50,60};
		//for integer arry
		
		System.out.println(a);
		
		for(int i:a)
		{
			System.out.println(i);
		}
		
		char ch[] = {'j','a','v','a'};
		System.out.println(ch);
	}

}
